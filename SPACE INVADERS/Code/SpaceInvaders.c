               ////////////  MISSILE_LASER_SHOOT_CHECK ///////////////////// 
 
 void MISSILE_LASER_SHOOT_CHECK(void)
 {
	 int i,j,k,h;
	 for(i=0;i<4;i++)
	 {
		 for(j=0;j<4;j++)
		 {
		 if((missile[i].X+1>=enemy[j].X+2)&&(missile[i].X+1<=enemy[j].X+14)&&(enemy[j].Y==missile[i].Y))
		 {
			 Sound_Killed();
			 
			 missile[i].LIFE=0;
			 enemy[j].LIFE=0;
		   Enemy_Laser[j].LIFE=0;
			 Explosion[j].LIFE=1;
			 Explosion[j].X=enemy[j].X;
			 Explosion[j].Y=enemy[j].Y;
			 Explosion[j].image=SmallExplosion0;
		 }
	 }
		 if(missile[i].Y==0)
		 {
			 missile[i].LIFE=0;
		 }
	 }
	 
	 
	 for(k=0;k<4;k++)
	 {
		 if((Enemy_Laser[k].X>=Player_Ship.X+3)&&(Enemy_Laser[k].X<=Player_Ship.X+14)&&(Enemy_Laser[k].Y==41))
		 {
			 Sound_Explosion();
			 Player_Ship.LIFE=0;
			 Player_explosion.LIFE=1;
			 Player_explosion.X=Player_Ship.X;
			 Player_explosion.Y=Player_Ship.Y;
		 }
	 }
	 
	 for(h=0;h<4;h++)
	 {
		 if(enemy[h].Y>=42)
		 {
			 Player_Ship.LIFE=0;
		 }
	 }
 }
 
 
